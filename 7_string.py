#make example of about 10 pre defined function which can manipulate string with description
name = "I am Pranis 10"
print(name.upper()) # convert string to upper case
#output= I AM PRANIS 10
print(name.lower()) # convert string to lower case
#output= iam pranis 10
print(name.capitalize()) # converts first character into upper case
#output= I am pranis 10
print(name.title()) # converts first character of each word into upper case
#output= I Am Pranis 10
print(name.swapcase()) # swap upper and lower case
#output= i AM prANIS 10

print(name.strip()) # remove leading and trailing spaces
#output= 
print(name.casefold()) # converts string into lower case
print(name.endswith()) # returns true if thr string ends with specific value
print(name.startswith()) # returns true if the string starts with specific value
print(name.center()) # returns a string centered in a field of specified width
print(name.ljust()) # returns a string left-justified in a field of specified width